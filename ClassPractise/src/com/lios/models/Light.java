package com.lios.models;

/**
 * @author liaiguang
 */
public class Light {
    public void on() {
        System.out.println("Light is on!");
    }

    public void off() {
        System.out.println("Light is off!");
    }

    public void brighten() {
        System.out.println("Light is more bright!");
    }

    public void dim() {
        System.out.println("Light is less bright!");
    }
}
